// Helper functions
export function getCountryByCode(code) {
  return countries.find(c => c.cca3 === code || c.cca2 === code.toUpperCase())
}

export function getCountriesByRegion(region) {
  return countries.filter(c => c.region === region)
}

export function searchCountries(query) {
  const q = query.toLowerCase()
  return countries.filter(c =>
    c.name.common.toLowerCase().includes(q) ||
    c.name.official.toLowerCase().includes(q) ||
    (c.capital && c.capital.some(cap => cap.toLowerCase().includes(q)))
  )
}

export function getRegions() {
  return [...new Set(countries.map(c => c.region))].sort()
}

export function formatPopulation(num) {
  if (num >= 1e9) return (num / 1e9).toFixed(1) + 'B'
  if (num >= 1e6) return (num / 1e6).toFixed(1) + 'M'
  if (num >= 1e3) return (num / 1e3).toFixed(1) + 'K'
  return num.toString()
}

export function formatArea(num) {
  return num.toLocaleString() + ' km²'
}

export function getLanguages(country) {
  return Object.values(country.languages || {}).join(', ')
}

export function getCurrencies(country) {
  return Object.values(country.currencies || {})
    .map(c => `${c.name} (${c.symbol})`)
    .join(', ')
}

export function getBorderCountries(country) {
  if (!country.borders || country.borders.length === 0) return []
  return country.borders
    .map(code => countries.find(c => c.cca3 === code))
    .filter(Boolean)
}
